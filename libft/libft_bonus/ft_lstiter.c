#include "libft.h"

/*
	DESCRIPTION :
	The function ft_lstiter applies the function f passed as parameter
	to the content of each node of a given list.

	RETURN VALUE :
	None.
*/

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	if (!lst || !f)
		return ;
	while (lst)
	{
		f(lst->content);
		lst = lst->next;
	}
}
