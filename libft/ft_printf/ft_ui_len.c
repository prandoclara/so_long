#include "ft_printf.h"

int	ft_ui_len(unsigned int n)
{
	int	len;

	len = 0;
	if (n == 0)
	{
		len++;
	}
	while (n > 0)
	{
		len++;
		n /= 10;
	}
	return (len);
}
